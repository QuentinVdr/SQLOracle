DROP TABLE BSKT_JOUEURS CASCADE CONSTRAINTS;

DROP TABLE BSKT_EFFECTIFS CASCADE CONSTRAINTS;

DROP TABLE BSKT_CHAMPIONNATS CASCADE CONSTRAINTS;

DROP TABLE BSKT_EQUIPES CASCADE CONSTRAINTS;

DROP TABLE BSKT_MATCHS CASCADE CONSTRAINTS;

DROP TABLE BSKT_STATS_MATCHS CASCADE CONSTRAINTS;

CREATE TABLE BSKT_JOUEURS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_JOUEURS PRIMARY KEY,
    NOM VARCHAR2(50) NOT NULL,
    PRENOM VARCHAR2(30) NOT NULL,
    TAILLE NUMBER(3, 0) NULL -- en centimetres
    CONSTRAINT CK_TAILLE CHECK(TAILLE >= 0)
);

CREATE TABLE BSKT_CHAMPIONNATS (
    CODE CHAR(8) CONSTRAINT PK_CHAMPIONNATS PRIMARY KEY,
    LIBELLE VARCHAR2(30) NOT NULL,
    SAISON VARCHAR2(9) DEFAULT 'xxxx-xxxx' NOT NULL,
    DEBUT DATE NOT NULL,
    FIN DATE NOT NULL,
    CONSTRAINT UN_CHAMPIONNATS UNIQUE(LIBELLE, SAISON)
);

CREATE TABLE BSKT_EQUIPES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_EQUIPES PRIMARY KEY,
    NOM VARCHAR2(50) NOT NULL CONSTRAINT UN_NOM UNIQUE
);

CREATE TABLE BSKT_EFFECTIFS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_EFFECTIFS PRIMARY KEY,
    CHAMPIONNAT CHAR(8) NOT NULL CONSTRAINT FK_EFFECTIFS_CHAMPIONNATS REFERENCES BSKT_CHAMPIONNATS(CODE),
    EQUIPE NUMBER NOT NULL CONSTRAINT FK_EFFECTIFS_EQUIPES REFERENCES BSKT_EQUIPES(ID),
    JOUEUR NUMBER NOT NULL CONSTRAINT FK_EFFECTIFS_JOUEURS REFERENCES BSKT_JOUEURS(ID),
    CONSTRAINT UN_EFFECTIFS_EQUIPE UNIQUE (CHAMPIONNAT, EQUIPE, JOUEUR)
);

CREATE TABLE BSKT_MATCHS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_MATCHS PRIMARY KEY,
    CHAMPIONNAT CHAR(8) NOT NULL CONSTRAINT FK_MATCHS_CHAMPIONNATS REFERENCES BSKT_CHAMPIONNATS(CODE),
    DATE_MATCH DATE NOT NULL,
    EQUIPE_1 NUMBER NOT NULL CONSTRAINT FK_MATCHS_EQUIPES_1 REFERENCES BSKT_EQUIPES(ID),
    EQUIPE_2 NUMBER NOT NULL CONSTRAINT FK_MATCHS_EQUIPES_2 REFERENCES BSKT_EQUIPES(ID),
    SCORE_EQ_1 NUMBER(3) NULL,
    SCORE_EQ_2 NUMBER(3) NULL,
    MVP NUMBER NULL CONSTRAINT FK_MATCHS_JOUEURS REFERENCES BSKT_JOUEURS(ID)
);

CREATE TABLE BSKT_STATS_MATCHS(
    MATCH NUMBER CONSTRAINT FK_STATS_MATCHS_MATCHS REFERENCES BSKT_MATCHS(ID),
    JOUEUR NUMBER CONSTRAINT FK_STATS_MATCHS_JOUEURS REFERENCES BSKT_JOUEURS(ID),
    TEMPS_JEU NUMBER(2), -- en minutes
    LF_TENTES NUMBER(2), -- lancers francs tentés
    LF_REUSSIS NUMBER(2), -- lancers francs réussis
    T2_TENTES NUMBER(2), -- tirs à 2 points tentés
    T2_REUSSIS NUMBER(2), -- tirs à 2 points réussis
    T3_TENTES NUMBER(2), -- tirs à 3 points tentés
    T3_REUSSIS NUMBER(2), -- tirs à 3 points réussis
    PASSES_DEC NUMBER(2), -- passes décisives
    INTER NUMBER(2), -- interceptions
    FAUTES NUMBER(1),
    CONSTRAINT PK_STATS_MATCHS PRIMARY KEY (MATCH, JOUEUR)
);

